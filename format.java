public class format {
    public static void main(String[] args){
        String c = "telugu sravani";
        int t = 1010;
        float g = 23;
        System.out.println(String.format("%s", "telugu sravani"));
        System.out.println(String.format("%x", t));
        System.out.println(String.format("%f", g));
    }
}
